function jishowfunction() {
    var pbox = document.querySelector(".subscribepremium");

    if (pbox.style.display === "none") {
        pbox.style.display = "block";
    } else {
        pbox.style.display = "block";
    }

}

function jiclose() {
    var pbox = document.querySelector(".subscribepremium");
    if (pbox.style.display === "block") {
        pbox.style.display = "none";
    } else {
        pbox.style.display = "none";
    }

}